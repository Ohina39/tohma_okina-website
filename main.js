function OnLinkClick1() {
    var Popup1 = document.getElementById("PopupFrame1");
    var Top_mozi = document.getElementById("PopupLink1");

    var Popup2 = document.getElementById("PopupFrame2");
    var News_mozi = document.getElementById("PopupLink2");

    var Popup3 = document.getElementById("PopupFrame3");
    var Profile_mozi = document.getElementById("PopupLink3");

    var Popup4 = document.getElementById("PopupFrame4");
    var People_mozi = document.getElementById("PopupLink4");

    var Popup5 = document.getElementById("PopupFrame5");
    var Music_mozi = document.getElementById("PopupLink5");

    var Popup6 = document.getElementById("PopupFrame6");
    var Blog_mozi = document.getElementById("PopupLink6");

    Top_mozi.style.color = '#a0d3f7';
    
    if (Popup1.style.visibility === "hidden") {
        Popup1.style.visibility = "visible";

        Top_mozi.style.color = '#e3eb9b';
        Top_mozi.style.pointerEvents = "none";

        Popup2.style.visibility = "hidden";
        News_mozi.style.pointerEvents = "auto";
        News_mozi.style.color = '#ffffff';

        Popup3.style.visibility = "hidden";
        Profile_mozi.style.pointerEvents = "auto";
        Profile_mozi.style.color = '#ffffff';

        Popup4.style.visibility = "hidden";
        People_mozi.style.pointerEvents = "auto";
        People_mozi.style.color = '#ffffff';

        Popup5.style.visibility = "hidden";
        Music_mozi.style.pointerEvents = "auto";
        Music_mozi.style.color = '#ffffff';

        Popup6.style.visibility = "hidden";
        Blog_mozi.style.pointerEvents = "auto";
        Blog_mozi.style.color = '#ffffff';

    } else {
        Popup1.style.visibility = "hidden";
        Top_mozi.style.color = '#ffffff';
    }

    Popup1.style.left = "300px";
    Popup1.style.top = "10px";
    
}

